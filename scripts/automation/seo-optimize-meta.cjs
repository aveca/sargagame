#!/usr/bin/env node
/**
 * SEO Optimize Meta — Improve titles/descriptions based on GSC CTR data.
 * Reads audit-summary.json, identifies low-CTR pages, generates optimized
 * title/description overrides, and patches vite.config.js.
 *
 * Safety: max 5 pages per run. Dry-run support.
 */
const { readFileSync, writeFileSync, existsSync } = require('fs')
const { resolve } = require('path')
const { BEACHES } = require('./lib/config.cjs')
const { DRY_RUN, LIMITS, appendLog } = require('./lib/safety.cjs')

const AUDIT_PATH = resolve(__dirname, 'data', 'audit-summary.json')
const VITE_CONFIG_PATH = resolve(__dirname, '..', '..', 'vite.config.js')
const OVERRIDES_PATH = resolve(__dirname, 'data', 'meta-overrides.json')

/**
 * Generate an optimized title using top queries from GSC.
 */
function generateTitle(beach, island, topQueries) {
  const year = new Date().getFullYear()
  // Include the top query keyword if it adds value
  const topQuery = topQueries[0]?.query || ''
  const hasIslandKeyword = topQuery.toLowerCase().includes(island.toLowerCase())
  const hasSargasses = topQuery.toLowerCase().includes('sargasse')

  // If top query mentions "plage" or beach-specific terms, lead with that
  if (topQuery.toLowerCase().includes('plage')) {
    return `Plage ${beach.name} — Sargasses ${island} ${year}`
  }
  // Default optimized: add year for freshness signal
  return `${beach.name} — Sargasses ${island} aujourd'hui ${year}`
}

/**
 * Generate an optimized meta description using top queries.
 */
function generateDescription(beach, island, topQueries) {
  const queryTerms = topQueries.slice(0, 3).map(q => q.query).join(', ')
  const base = `Sargasses à ${beach.name} (${beach.commune}, ${island}) : plage propre ou à éviter aujourd'hui ? Prévisions 7 jours, indice AFAI en temps réel et carte interactive.`
  return base
}

function main() {
  console.log(`=== SEO Optimize Meta ${DRY_RUN ? '(DRY RUN)' : ''} ===\n`)

  if (!existsSync(AUDIT_PATH)) {
    console.log('No audit-summary.json found. Run seo-audit.cjs first.')
    process.exit(0)
  }

  const audit = JSON.parse(readFileSync(AUDIT_PATH, 'utf-8'))
  const overrides = { titles: {}, descriptions: {}, generatedAt: new Date().toISOString() }
  let changeCount = 0

  for (const [siteKey, siteData] of Object.entries(audit.sites)) {
    const lowCtr = siteData.findings.lowCtrPages || []
    console.log(`${siteData.domain}: ${lowCtr.length} low-CTR pages found`)

    for (const page of lowCtr) {
      if (changeCount >= LIMITS.MAX_META_CHANGES_PER_RUN) {
        console.log(`  ⚠ Limit reached (${LIMITS.MAX_META_CHANGES_PER_RUN} changes per run)`)
        break
      }

      // Match page URL to a beach slug
      const urlMatch = page.page.match(/\/plages\/([^/]+)\//)
      if (!urlMatch) continue
      const slug = urlMatch[1]
      const beach = BEACHES.find(b => b.slug === slug)
      if (!beach) continue

      const island = beach.island === 'mq' ? 'Martinique' : 'Guadeloupe'
      const newTitle = generateTitle(beach, island, page.topQueries || [])
      const newDesc = generateDescription(beach, island, page.topQueries || [])
      const currentTitle = `${beach.name} — Sargasses ${island} aujourd'hui`

      if (newTitle === currentTitle) continue

      overrides.titles[slug] = newTitle
      overrides.descriptions[slug] = newDesc
      changeCount++

      console.log(`  + ${slug}:`)
      console.log(`    Title: "${currentTitle}" → "${newTitle}"`)
      console.log(`    CTR: ${page.actualCtr}% (expected ${page.expectedCtr}%), pos ${page.avgPosition}`)
    }
  }

  if (changeCount === 0) {
    console.log('\nNo meta optimizations needed.')
    appendLog({ script: 'seo-optimize-meta', action: 'no-changes', changesCount: 0 })
    return
  }

  // Write overrides JSON (consumed by vite.config.js at build time)
  if (DRY_RUN) {
    console.log(`\n[DRY RUN] Would write ${changeCount} overrides`)
  } else {
    writeFileSync(OVERRIDES_PATH, JSON.stringify(overrides, null, 2))
    console.log(`\n✓ ${changeCount} meta overrides written to ${OVERRIDES_PATH}`)

    // Patch vite.config.js to read overrides if not already patched
    let viteConfig = readFileSync(VITE_CONFIG_PATH, 'utf-8')
    if (!viteConfig.includes('meta-overrides.json')) {
      // Insert override loading after the beaches array declaration
      const marker = `const domainMQ = 'sargasses-martinique.com'`
      const overrideCode = `
          // SEO meta overrides (generated by seo-optimize-meta.cjs)
          let _metaOverrides = { titles: {}, descriptions: {} }
          try { _metaOverrides = JSON.parse(readFileSync(resolve(__dirname, 'scripts/automation/data/meta-overrides.json'), 'utf-8')) } catch {}
          ${marker}`
      viteConfig = viteConfig.replace(marker, overrideCode)

      // Patch title/desc generation to use overrides
      const oldTitle = 'const beachTitle = `${b.name} — Sargasses ${island} aujourd\'hui`'
      const newTitle = 'const beachTitle = _metaOverrides.titles[b.slug] || `${b.name} — Sargasses ${island} aujourd\'hui`'
      viteConfig = viteConfig.replace(oldTitle, newTitle)

      const oldDesc = "const beachDesc = `État des sargasses à ${b.name} (${b.commune}, ${island}) aujourd'hui. Plage propre ou à éviter ? Consultez la fiche, les prévisions 7 jours et la carte en temps réel.`"
      const newDesc = "const beachDesc = _metaOverrides.descriptions[b.slug] || `État des sargasses à ${b.name} (${b.commune}, ${island}) aujourd'hui. Plage propre ou à éviter ? Consultez la fiche, les prévisions 7 jours et la carte en temps réel.`"
      viteConfig = viteConfig.replace(oldDesc, newDesc)

      writeFileSync(VITE_CONFIG_PATH, viteConfig)
      console.log('✓ vite.config.js patched to read meta overrides')
    }
  }

  appendLog({
    script: 'seo-optimize-meta',
    action: DRY_RUN ? 'dry-run' : 'meta-optimized',
    changesCount: changeCount,
    overrides: Object.keys(overrides.titles),
  })
}

main()
