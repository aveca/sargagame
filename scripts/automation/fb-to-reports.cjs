#!/usr/bin/env node
/**
 * fb-to-reports.cjs — Bridge FB signals into the app's community reports feed.
 *
 * Reads:  scripts/automation/data/fb-signals.json (from fb-analyze.cjs)
 * Writes: public/api/community/fb-reports.json (fetched by Sargasses_PROD.jsx)
 *
 * Output shape mirrors the Google Apps Script /beach_reports endpoint so the
 * app can fetch both and merge without format translation:
 *   { updatedAt, source: 'facebook', reports: { [beachId]: {avoid, moderate, clean, total, samples} } }
 *
 * The app already uses a ≥3-reports threshold before FB signals affect ranking
 * (Sargasses_PROD.jsx line ~2423), so single-post beaches won't move the hero
 * pick until we have multiple independent reports — protects against astroturfing.
 *
 * Run: node scripts/automation/fb-to-reports.cjs
 * Chain: fb-scrape → fb-analyze → fb-to-reports → commit → deploy
 */
const fs = require('fs')
const path = require('path')

const SIGNALS_PATH = path.join(__dirname, 'data', 'fb-signals.json')
const OUT_PATH = path.join(__dirname, '..', '..', 'public', 'api', 'community', 'fb-reports.json')

function main() {
  let signals
  try { signals = JSON.parse(fs.readFileSync(SIGNALS_PATH, 'utf-8')) }
  catch { console.error('✗ No fb-signals.json — run fb-analyze first'); process.exit(1) }

  const reports = {}
  for (const [beachId, sig] of Object.entries(signals.communitySignals || {})) {
    reports[beachId] = {
      avoid: sig.avoid || 0,
      moderate: sig.moderate || 0,
      clean: sig.clean || 0,
      total: sig.total || 0,
      lastReportAt: signals._generatedAt,
      samples: (sig.samples || []).map(s => ({
        text: s.text,
        date: s.date,
        source: 'facebook', // distinguishes from in-app reports
      })),
    }
  }

  const out = {
    _comment: 'FB-sourced community signals. Merge with /beach_reports endpoint. Generated by fb-to-reports.cjs.',
    updatedAt: new Date().toISOString(),
    source: 'facebook',
    beachCount: Object.keys(reports).length,
    reports,
  }

  if (!fs.existsSync(path.dirname(OUT_PATH))) fs.mkdirSync(path.dirname(OUT_PATH), { recursive: true })
  fs.writeFileSync(OUT_PATH, JSON.stringify(out, null, 2), 'utf-8')

  console.log(`✓ Wrote ${Object.keys(reports).length} FB-sourced beach reports`)
  console.log(`  Output: ${OUT_PATH}`)
  for (const [id, r] of Object.entries(reports)) {
    console.log(`  • ${id}: ${r.total} reports (avoid=${r.avoid}, mod=${r.moderate}, clean=${r.clean})`)
  }
}

try { main() }
catch (e) { console.error('fb-to-reports error:', e.message); process.exit(1) }
