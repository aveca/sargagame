#!/usr/bin/env node
/**
 * SEO Orphan Detector — Find sitemap pages with zero GSC traffic.
 *
 * An orphan page is one Google knows about (it's in the sitemap and
 * served 200) but that earns ~no impressions. This is usually because:
 *   1. No internal links from high-authority pages → low crawl priority
 *   2. Weak title/meta → no SERP impressions even when indexed
 *   3. Indexed but ranked outside top 100 (effectively invisible)
 *
 * This script cross-references the sitemap with audit-full.json and
 * outputs orphan-pages.json with a prioritized list + recommendations
 * for which high-authority pages should link to each orphan.
 *
 * Usage: node scripts/automation/seo-orphan-detector.cjs
 */
const { readFileSync, writeFileSync, existsSync } = require('fs')
const { resolve } = require('path')
const { appendLog } = require('./lib/safety.cjs')
const { SITES } = require('./lib/config.cjs')

const DATA_DIR = resolve(__dirname, 'data')
const AUDIT_PATH = resolve(DATA_DIR, 'audit-full.json')
const OUT_PATH = resolve(DATA_DIR, 'orphan-pages.json')

// The real sitemap (with all beach + editorial pages) is generated by the
// vite plugin into dist/ then prepare-ftp.cjs copies it to *-ftp/. The file
// in public/ is only the 15-URL source stub. Prefer FTP → dist → public.
const ROOT = resolve(__dirname, '..', '..')

const ORPHAN_IMPRESSION_THRESHOLD = 5      // <= 5 impressions over the audit window = orphan
const HIGH_AUTHORITY_IMPRESSION_THRESHOLD = 1000 // pages above this are link donors

function loadAudit() {
  if (!existsSync(AUDIT_PATH)) {
    console.error(`audit-full.json not found at ${AUDIT_PATH}`)
    process.exit(1)
  }
  return JSON.parse(readFileSync(AUDIT_PATH, 'utf-8'))
}

const SITE_SITEMAP = {
  mq: { name: 'sitemap-martinique.xml', ftp: 'martinique-ftp' },
  gp: { name: 'sitemap-guadeloupe.xml', ftp: 'guadeloupe-ftp' },
}

function parseSitemap(siteKey) {
  const cfg = SITE_SITEMAP[siteKey]
  if (!cfg) return []
  // Prefer FTP folder (post-build, post-prepare) → dist → public stub
  const candidates = [
    resolve(ROOT, cfg.ftp, cfg.name),
    resolve(ROOT, 'dist', cfg.name),
    resolve(ROOT, 'public', cfg.name),
  ]
  for (const path of candidates) {
    if (!existsSync(path)) continue
    const xml = readFileSync(path, 'utf-8')
    const urls = []
    const re = /<loc>([^<]+)<\/loc>/g
    let m
    while ((m = re.exec(xml)) !== null) urls.push(m[1].trim())
    // Filter to this site's domain so the cross-domain sitemap doesn't pollute
    const domain = SITES[siteKey].domain
    return urls.filter(u => u.includes(domain))
  }
  return []
}

function detectOrphans(siteKey, sitemapUrls, performance) {
  const orphans = []
  const linkDonors = []
  for (const url of sitemapUrls) {
    const m = performance[url]
    const impressions = m ? m.impressions : 0
    const clicks = m ? m.clicks : 0
    if (impressions <= ORPHAN_IMPRESSION_THRESHOLD) {
      orphans.push({ url, impressions, clicks, kind: classify(url) })
    }
    if (impressions >= HIGH_AUTHORITY_IMPRESSION_THRESHOLD) {
      linkDonors.push({ url, impressions, clicks })
    }
  }
  // Donors sorted by impressions desc → top of the list = best link source
  linkDonors.sort((a, b) => b.impressions - a.impressions)
  // Orphans sorted by kind priority then alpha (editorials first — they're
  // the year-round SEO bet and the easiest copy wins)
  const kindRank = { editorial: 0, beach: 1, other: 2 }
  orphans.sort((a, b) => kindRank[a.kind] - kindRank[b.kind] || a.url.localeCompare(b.url))
  return { orphans, linkDonors }
}

function classify(url) {
  const path = url.replace(/^https?:\/\/[^/]+/, '')
  if (path.startsWith('/plages/')) return 'beach'
  // Editorial pages live at top level (e.g. /comprendre-sargasses/)
  if (/^\/[a-z][a-z0-9-]+\/?$/.test(path) && path !== '/en/') return 'editorial'
  return 'other'
}

function recommendInjections(orphans, linkDonors) {
  // For each orphan, suggest 2 link donors. Editorials get linked from
  // the homepage + carte; beaches get linked from the editorial covering
  // their region (best-effort).
  if (linkDonors.length === 0) return orphans
  const top2 = linkDonors.slice(0, 2).map(d => d.url)
  return orphans.map(o => ({ ...o, recommendedLinkSources: top2 }))
}

function main() {
  console.log('=== SEO Orphan Detector ===\n')
  const audit = loadAudit()
  const report = { generatedAt: new Date().toISOString(), sites: {} }
  let totalOrphans = 0

  for (const [siteKey, siteData] of Object.entries(audit.sites || {})) {
    const sitemapUrls = parseSitemap(siteKey)
    if (sitemapUrls.length === 0) {
      console.warn(`[${siteKey}] no sitemap found — skipping`)
      continue
    }
    const performance = siteData.performance || {}
    const { orphans, linkDonors } = detectOrphans(siteKey, sitemapUrls, performance)
    const enriched = recommendInjections(orphans, linkDonors)

    const editorials = enriched.filter(o => o.kind === 'editorial')
    const beaches = enriched.filter(o => o.kind === 'beach')

    console.log(`[${siteKey}] sitemap=${sitemapUrls.length}  orphans=${enriched.length}  donors=${linkDonors.length}`)
    console.log(`         editorials=${editorials.length}  beaches=${beaches.length}  other=${enriched.length - editorials.length - beaches.length}`)

    if (editorials.length > 0) {
      console.log(`         top editorial orphans:`)
      for (const o of editorials.slice(0, 5)) {
        const short = o.url.replace(/^https?:\/\/[^/]+/, '')
        console.log(`           ${short.padEnd(40)} impr=${o.impressions} clicks=${o.clicks}`)
      }
    }

    report.sites[siteKey] = {
      sitemapSize: sitemapUrls.length,
      orphanCount: enriched.length,
      orphansByKind: {
        editorial: editorials.length,
        beach: beaches.length,
        other: enriched.length - editorials.length - beaches.length,
      },
      linkDonors: linkDonors.slice(0, 10),
      orphans: enriched,
    }
    totalOrphans += enriched.length
  }

  writeFileSync(OUT_PATH, JSON.stringify(report, null, 2))
  console.log(`\nWrote ${OUT_PATH} (${totalOrphans} orphans across ${Object.keys(report.sites).length} sites)`)

  appendLog({
    script: 'seo-orphan-detector',
    action: 'scan',
    sites: Object.keys(report.sites).length,
    totalOrphans,
  })
}

main()
