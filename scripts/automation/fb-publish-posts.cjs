#!/usr/bin/env node
/**
 * fb-publish-posts.cjs — Surface raw FB posts (photos + comments) per beach
 * to the frontend, so beach sheets can show real visitor testimony.
 *
 * Reads:  scripts/automation/data/fb-feed.json
 * Writes: public/api/community/fb-posts.json (consumed by Sargasses_PROD.jsx)
 *
 * This is separate from fb-to-reports.cjs which produces aggregated signal
 * counts (avoid/moderate/clean). That feeds the ranking system. THIS script
 * publishes the raw material — photos, quotes, authors — so the UI can show
 * "2 sargassum photos posted to the Martinique group yesterday".
 *
 * Legal posture:
 *   - Photos are hotlinked (scontent CDN URLs), not re-hosted. If CDN rotates
 *     them we lose display but don't infringe. Consider caching if UX suffers.
 *   - Author name + source link are always displayed — no anonymization.
 *   - Cap 14 days old, 3 posts per beach (fresh, non-overwhelming).
 *   - No personal data beyond public FB group content (authors post publicly).
 *
 * Schema:
 *   {
 *     updatedAt: ISO,
 *     postsByBeach: {
 *       [beachId]: [{
 *         author, text (≤220 chars), photos[] (≤3), commentSample (1 quote),
 *         inferredStatus, sourceUrl, scrapedAt, group
 *       }]
 *     }
 *   }
 *
 * Usage: node scripts/automation/fb-publish-posts.cjs
 */
const fs = require('fs')
const path = require('path')

const FEED_PATH = path.join(__dirname, 'data', 'fb-feed.json')
const OUT_PATH = path.join(__dirname, '..', '..', 'public', 'api', 'community', 'fb-posts.json')

const MAX_AGE_DAYS = 14
const MAX_PER_BEACH = 3
const MAX_TEXT_LEN = 220
const MAX_PHOTOS = 3

function loadFeed() {
  try { return JSON.parse(fs.readFileSync(FEED_PATH, 'utf-8')) }
  catch { return { posts: [] } }
}

function bestPostText(p) {
  return [p.postText, p.question, ...(p.texts || [])]
    .filter(Boolean).map(s => String(s).trim())
    .sort((a, b) => b.length - a.length)[0] || ''
}

function publishablePost(p) {
  if (!p.beachId) return false
  const age = Date.now() - new Date(p.scrapedAt || 0).getTime()
  if (age > MAX_AGE_DAYS * 86400 * 1000) return false
  const hasPhotos = (p.photoSamples || p.images || []).length > 0
  const hasText = bestPostText(p).length >= 20
  const hasComment = (p.comments || []).length > 0
  return hasPhotos || hasText || hasComment
}

function pickBestComment(comments) {
  if (!Array.isArray(comments) || !comments.length) return null
  // Prefer comments with a status signal (terrain truth), else longest
  const withSignal = comments.filter(c => c.signal)
  const chosen = withSignal[0] || comments.sort((a, b) => (b.text?.length || 0) - (a.text?.length || 0))[0]
  return chosen?.text ? chosen.text.slice(0, 200) : null
}

function transform(post) {
  const rawText = bestPostText(post)
  const photoSources = (post.photoSamples || []).length ? post.photoSamples : (post.images || []).map(i => i.src || i)
  return {
    author: post.author || 'Anonyme',
    text: rawText.slice(0, MAX_TEXT_LEN),
    textTruncated: rawText.length > MAX_TEXT_LEN,
    photos: photoSources.slice(0, MAX_PHOTOS),
    commentSample: pickBestComment(post.comments),
    commentCount: (post.comments || []).length,
    inferredStatus: post.inferredStatus || null,
    sourceUrl: post.sourceUrl,
    scrapedAt: post.scrapedAt,
    group: post.group,
    island: post.island,
  }
}

function main() {
  const feed = loadFeed()
  const eligible = (feed.posts || []).filter(publishablePost)

  const postsByBeach = {}
  for (const post of eligible) {
    if (!postsByBeach[post.beachId]) postsByBeach[post.beachId] = []
    postsByBeach[post.beachId].push(transform(post))
  }

  // Sort + cap per beach
  for (const beachId of Object.keys(postsByBeach)) {
    postsByBeach[beachId].sort((a, b) => new Date(b.scrapedAt) - new Date(a.scrapedAt))
    postsByBeach[beachId] = postsByBeach[beachId].slice(0, MAX_PER_BEACH)
  }

  const out = {
    _comment: 'Raw FB posts (photos + author + comments) for in-app display on beach sheets. Not to be confused with fb-reports.json which is aggregated signal counts. Generated by fb-publish-posts.cjs.',
    updatedAt: new Date().toISOString(),
    source: 'facebook',
    beachCount: Object.keys(postsByBeach).length,
    postCount: Object.values(postsByBeach).reduce((n, arr) => n + arr.length, 0),
    postsByBeach,
  }

  if (!fs.existsSync(path.dirname(OUT_PATH))) fs.mkdirSync(path.dirname(OUT_PATH), { recursive: true })
  fs.writeFileSync(OUT_PATH, JSON.stringify(out, null, 2), 'utf-8')

  console.log(`✓ Published ${out.postCount} FB posts across ${out.beachCount} beaches`)
  for (const [id, arr] of Object.entries(postsByBeach)) {
    const p = arr[0]
    console.log(`  • ${id} (${arr.length} posts): ${p.author} — ${(p.text || p.commentSample || '').slice(0, 60)}…`)
  }
}

main()
